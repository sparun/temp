% -------------------------------------------------------------------
% This function clears all variables, closes all figure windows, and
% clears the command window
% -------------------------------------------------------------------
% psy_allclear()
% REQUIRED INPUTS
%  None
% OPTIONAL INPUTS
%  None
% OUTPUTS
%  None
% METHOD
%     
% NOTES
% 
% EXAMPLE
%  psy_allclear();
%  will clear variables, figures, and command window
% REQUIRED SUBROUTINES
%  None
%
% Zhivago KA
% 07 Dec 2010

function psy_allclear()
clear all; close all; clc
return;